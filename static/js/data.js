const joinTexts = (resource) => {
  const temp = [[]];
  let texts = [];
  texts.push(...resource.description);
  texts.push(...resource.uses);
  texts.push(...resource.economics);

  while (texts.length) {
    if (temp[temp.length - 1].length < 2)
      temp[temp.length - 1].push(texts.shift());
    else {
      temp.push([]);
      temp[temp.length - 1].push(texts.shift());
    }
  }
  return temp;
};

